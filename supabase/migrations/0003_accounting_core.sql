-- ---------- Ledger tables ----------
create table if not exists public.accounts (
  id          uuid primary key default gen_random_uuid(),
  code        text not null unique,
  name        text not null,
  type        text not null check (type in ('asset','liability','equity','income','expense')),
  is_active   boolean not null default true,
  description text,
  created_at  timestamptz default now()
);

create table if not exists public.journal_entries (
  id                uuid primary key default gen_random_uuid(),
  entry_date        date not null default ((now() at time zone 'Pacific/Auckland'))::date,
  memo              text,
  source_type       text not null default 'manual',
  source_id         uuid,
  reverses_entry_id uuid references public.journal_entries(id),
  created_by        uuid references public.staff(id),
  created_at        timestamptz default now()
);

create table if not exists public.journal_lines (
  id         uuid primary key default gen_random_uuid(),
  entry_id   uuid not null references public.journal_entries(id) on delete cascade,
  account_id uuid not null references public.accounts(id),
  debit      numeric not null default 0 check (debit  >= 0) check (debit  = round(debit, 2)),
  credit     numeric not null default 0 check (credit >= 0) check (credit = round(credit, 2)),
  memo       text,
  constraint not_both_zero check (debit > 0 or credit > 0),
  constraint one_side_only check (debit = 0 or credit = 0)
);

create table if not exists public.credit_notes (
  id                 uuid primary key default gen_random_uuid(),
  credit_note_number bigint generated by default as identity not null,
  invoice_id         uuid references public.invoices(id),
  customer_id        uuid references public.customers(id),
  reason             text,
  subtotal           numeric not null default 0 check (subtotal = round(subtotal, 2)),
  gst                numeric not null default 0 check (gst      = round(gst, 2)),
  total              numeric not null default 0 check (total    = round(total, 2)),
  status             text not null default 'Issued',
  pdf_url            text,
  sent               boolean default false,
  sent_at            timestamptz,
  created_by         uuid references public.staff(id),
  created_at         timestamptz default now()
);

create table if not exists public.payments (
  id         uuid primary key default gen_random_uuid(),
  invoice_id uuid not null references public.invoices(id) on delete cascade,
  amount     numeric not null check (amount > 0) check (amount = round(amount, 2)),
  paid_date  date not null default ((now() at time zone 'Pacific/Auckland'))::date,
  method     text,
  created_by uuid references public.staff(id),
  created_at timestamptz default now()
);

-- ---------- Indexes ----------
create index if not exists journal_entries_date_idx  on public.journal_entries (entry_date);
create index if not exists journal_lines_entry_idx   on public.journal_lines   (entry_id);
create index if not exists journal_lines_account_idx on public.journal_lines   (account_id);
create index if not exists payments_invoice_idx      on public.payments        (invoice_id);

